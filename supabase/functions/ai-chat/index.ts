import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          {
            role: "system",
            content: `You are GETWAY AI Assistant — a helpful, professional chatbot for GETWAY AI CRM platform. 

GETWAY is an AI-powered Business Automation Platform founded in 2006, offering:
- AI CRM Software (lead management, sales pipeline, workflow automation)
- CPaaS Platform (Bulk SMS, RCS, WhatsApp Business API, Voice Broadcasting)
- AI Voice Agents (automated inbound/outbound calling, lead qualification)
- AI Websites & Sales Funnels
- IVR Calling System & Click-to-Call
- Email Marketing Automation
- School ERP & LMS

Key facts:
- Trusted by 20,000+ businesses
- 150+ White-Label Partners
- 20+ years of innovation
- Contact: connect@getway.in | +91 92555-22544
- HQ: SCO 24, 2nd Floor, Huda Market, Sector-10A, Gurugram, Haryana
- Website: getway.in

Keep responses concise (2-4 sentences max), helpful, and professional. If asked about pricing, suggest booking a demo. Always maintain a friendly tone. Answer in the language the user writes in (Hindi or English).`
          },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limited. Please try again shortly." }), {
          status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Service temporarily unavailable." }), {
          status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
